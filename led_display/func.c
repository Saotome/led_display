/*----------------------------------------------------------------------------
 * 
 * Argument	: uint16_t t
 * Return	: -
 *---------------------------------------------------------------------------*/
void wait_ms(uint16_t t)
{
	while(t--) _delay_ms(1);
}

/*----------------------------------------------------------------------------
*
* Argument	: -
* Return	: -
*---------------------------------------------------------------------------*/
void SetAll(bool s)
{
	VECTOR v;
	
	for( v.x = 0; v.x < X_MAX; v.x++) {
		for( v.y = 0; v.y < Y_MAX; v.y++){
			SetLed(v, s);
		}
	}
}

/*----------------------------------------------------------------------------
*
* Argument	: -
* Return	: -
*---------------------------------------------------------------------------*/
void SetAllLevel(uint8_t s)
{
	VECTOR v;
	
	for( v.x = 0; v.x < X_MAX; v.x++) {
		for( v.y = 0; v.y < Y_MAX; v.y++){
			SetLedLevel(v, s);
		}
	}
}
