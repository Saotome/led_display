/*****************************************************************************
 * Header Include                                                            *
 *****************************************************************************/
#include <avr/io.h>
#include "../drv.h"
#include "../func.h"

/*****************************************************************************
 * Variable                                                                  *
 *****************************************************************************/
static uint8_t pos = 0;

/*****************************************************************************
 * public function                                                           *
 *****************************************************************************/
/*----------------------------------------------------------------------------
 * とりあえず点滅するだけのテスト
 * Argument	: -
 * Return	: -
 *---------------------------------------------------------------------------*/
void flash()
{	
	VECTOR v;
	
	v.x = 0;
	v.y = pos;
	
	SetAll(false);
	SetLed(v ,true);
	wait_ms(100);
	pos++;
	pos %= Y_MAX;
}
